package Star_pattern;

public class ex9d {
	public static void main(String[] args) {
		int a=5; int b=1;
		for(int i=1;i<=a;i++) {
			for(int j=1;j<=b;j++) {
				System.out.print("*");
			}System.out.println();b++;
		}
		
		int c=5; int d=5;
		for(int i=1;i<=c;i++) {
			for(int j=1;j<=d;j++) {
				System.out.print("*");
			}System.out.println();d--;
		}
		
		int m=10; int n=1;
		for(int i=1;i<=m;i++) {
			for(int j=1;j<=n;j++) {
				System.out.print("*");
			}System.out.println();n++;
		}
		
		int x=10;int y=10;
		for(int i=1;i<=x;i++) {
		   for(int j=1;j<=y;j++) {
			   System.out.print("*");
		   }System.out.println();y--;
		}
		
		
		
		
	
	}}
